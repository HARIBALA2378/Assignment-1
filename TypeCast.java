class TypeCast
{
	public static void main(String args[]){
	//short to byte
	byte a = (byte)23456;
	System.out.println(a);
	System.out.println();

	//int to byte
	byte b = (byte)-1235567;
	System.out.println(b);
	System.out.println();

	//char to byte
	byte ab = '@';
	System.out.println(ab);
	System.out.println();

	//float to byte
	byte c = (byte)2.3f;
	System.out.println(c);
	System.out.println();

	//double to byte
	byte d = (byte)5.67;
	System.out.println(d);
	System.out.println();

	//int to short
	short e = (short)123345;
	System.out.println(e);
	System.out.println();

	//char to short
	short ef = 'w';
	System.out.println(ef);
	System.out.println();

	//float to short
	short f = (short)12.34242f;
	System.out.println(f);
	System.out.println();

	//double to short
	short g = (short)18.455455;
	System.out.println(g);
	System.out.println();

	//char to int
	int h = 'e';
	System.out.println(h);
	System.out.println();

	//float to int
	int i = (int)123.345f;
	System.out.println(i);
	System.out.println();

	//double to int
	int j = (int)23.345;
	System.out.println(i);
	System.out.println();

	//double to float
	float k = (float)56.76734345f;
	System.out.println(k);
	System.out.println();
	
	//float to long
	long l = (long)23.323f;
	System.out.println(l);
	System.out.println();
	 
	//double to long
	long m = (long)2.323423;
	System.out.println(m);
	System.out.println();
	}
}